  static List<CustomerReview> getReviewsForProduct(String productId) {
    // Use the product's hash to pick 3 different reviews deterministically
    final hash = productId.hashCode.abs();
    final count = _allReviews.length;
    final indices = [
      hash % count,
      (hash + 3) % count,
      (hash + 7) % count,
    ];
    return indices.map((i) => _allReviews[i]).toList();
  }

  static Map<String, Map<String, dynamic>> getAllProducts() {
    return Map.from(_products);
  }

  static Map<String, dynamic> getById(String productId) {
    final product = _products[productId];
    if (product == null) {
      return _products['product_001']!;
    }

    return {
      ...product,
      'relatedItems':
          _relatedItems.where((item) => item.id != productId).take(3).toList(),
      'customerReviews': getReviewsForProduct(productId),
    };
  }
